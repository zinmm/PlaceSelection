package com.zin.main;

public class SpinnerItem {
	private String name;
	private String pcode;
	private int cId;

	public int getcId() {
		return cId;
	}

	public void setcId(int cId) {
		this.cId = cId;
	}

	public String getName() {
		return name;
	}

	public String getPcode() {
		return pcode;
	}

	public void setName(String name) {
		this.name = name;
	}

	public void setPcode(String pcode) {
		this.pcode = pcode;
	}
}
